+ (NSString *)distanceTimeWithBeforeTime:(NSInteger)beTime{
    NSDate * beDate = [NSDate dateWithTimeIntervalSince1970:beTime];
    NSDateFormatter * df = [[NSDateFormatter alloc]init];
    [df setDateFormat:@"HH:mm"];
    NSString * timeStr = [df stringFromDate:beDate];
    NSString * distanceStr;
    
    if(beDate.isToday){//时间小于一天
        distanceStr = [NSString stringWithFormat:@"%@ %@",NSLocalizedString(@"today", nil),timeStr];
    }
    else if(beDate.isYesterday){
        distanceStr = [NSString stringWithFormat:@"%@ %@",NSLocalizedString(@"yesterday", nil),timeStr];
    }
    else if(beDate.isThisYear){
        [df setDateFormat:@"MM-dd HH:mm"];
        distanceStr = [df stringFromDate:beDate];
    }
    else{
        [df setDateFormat:@"yyyy-MM-dd HH:mm"];
        distanceStr = [df stringFromDate:beDate];
    }
    return distanceStr;
}


